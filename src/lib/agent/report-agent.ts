import { AgentContext, AgentResponse } from "./types";
import { getClient } from "./llm";

export async function reportAgent(input: string, ctx: AgentContext): Promise<AgentResponse> {
  const client = getClient();

  const systemPrompt = `??????????????????????????????

## ????
????????????????????????????????

## ????
- ?????????????"??????"?"?????????"?
- ???"??????"???????
- ???????????????

## ?????
1. ???????????
   - ????????????????
   - ???/????????????
   - ??????????????/?????????
   - ????????????

2. ???????????
   - ?????/????
   - ???????????
   - ?????????

3. ???????
   - ?????????????????????
   - ????????????????????

## ????
?????????????????Markdown???????
1. ?????2-3????????
2. ???????????????????
3. ?????????
4. ???????????
5. ??????

?????`;

  const userPrompt = `??????????????????

${ctx.dataSummary}

???????${input || "???????"}`;

  const res = await client.chat.completions.create({
    model: "deepseek-chat",
    messages: [
      { role: "system", content: systemPrompt },
      { role: "user", content: userPrompt },
    ],
    temperature: 0.3,
    max_tokens: 3000,
  });
  const text = res.choices[0]?.message?.content || "";

  try {
    const cleaned = text.replace(/```json|```/g, "").trim();
    const p = JSON.parse(cleaned);
    const secs = (p.sections || []).map((s: any) =>
      "## " + s.heading + "\n" + s.content + "\n> ????: " + (s.keyMetric || "N/A")
    ).join("\n\n");
    return {
      type: "report",
      content: "# " + (p.title || "??????") + "\n\n" + (p.summary || "") + "\n\n" + secs + "\n\n**????**: " + (p.conclusion || ""),
      followUp: p.followUp || [],
    };
  } catch {
    return {
      type: "report",
      content: text,
      followUp: ["?????????", "????????", "??PDF??"],
    };
  }
}
